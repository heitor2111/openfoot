/**
 * Níveis de reputação do clube
 * Categoriza-o com base em sua visibilidade, sucesso e reconhecimento no cenário do futebol.
 *
 * - LOCAL: Clube conhecido principalmente em sua região, cidade ou comunidade local. - ex.: Inter de Minas (MG)
 * - STATE: Clube competitivo em nível estadual. - ex.: Athletic Club (MG)
 * - NATIONAL: Clube com grande relevância em seu país. - ex.: Botafogo
 * - CONTINENTAL: Clube de destaque em seu continente. - ex.: Boca Juniors
 * - INTERNATIONAL: Clube de destaque em nível internacional. - ex.: Real Madrid
 */
export enum ClubReputation {
  LOCAL = 1,
  STATE = 2,
  NATIONAL = 3,
  CONTINENTAL = 4,
  INTERNATIONAL = 5,
}
